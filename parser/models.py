def infer_category(title: str, description: str) -> str:
    """Определяет категорию по тексту (для источников без явной категории)."""
    text = f"{title} {description}".lower()
    for cat, keywords in _CATEGORY_RULES:
        if any(kw in text for kw in keywords):
            return cat
    return "Прочее"


# --- Нормализация --------------------------------------------------------

_CITY_ALIASES = {
    "нур-султан": "Астана",
    "нурсултан": "Астана",
    "нур султан": "Астана",
    "astana": "Астана",
    "almaty": "Алматы",
    "алма-ата": "Алматы",
    "shymkent": "Шымкент",
    "вся страна": "Вся страна",
    "казахстан": "Вся страна",
    "по всему казахстану": "Вся страна",
}


def normalize_city(raw: Optional[str]) -> str:
    if not raw:
        return "Вся страна"
    key = raw.strip().lower()
    if key in _CITY_ALIASES:
        return _CITY_ALIASES[key]
    for city in CITIES:
        if city.lower() == key:
            return city
    # частичное совпадение
    for city in CITIES:
        if city.lower() in key:
            return city
    return "Вся страна"


def normalize_category(raw: Optional[str]) -> str:
    if not raw:
        return "Прочее"
    key = raw.strip().lower()
    for cat in CATEGORIES:
        if cat.lower() == key:
            return cat
    return raw.strip() if raw.strip() else "Прочее"


# --- Модель --------------------------------------------------------------

@dataclass
class Deal:
    """Одно предложение: скидка, акция, бонус или распродажа."""

    title: str                      # Заголовок предложения
    source: str                     # Имя источника (Magnum, Chocolife, ...)
    url: str                        # Ссылка на предложение
    category: str = "Прочее"        # Категория из CATEGORIES
    city: str = "Вся страна"        # Город из CITIES
    deal_type: str = "Акция"        # Тип из TYPES
    description: str = ""            # Краткое описание
    discount: Optional[str] = None  # Например "-50%" или "2=1"
    old_price: Optional[float] = None
    new_price: Optional[float] = None
    currency: str = "₸"
    image: Optional[str] = None     # URL картинки
    starts_at: Optional[str] = None # ISO дата начала
    ends_at: Optional[str] = None   # ISO дата окончания
    tags: list = field(default_factory=list)  # Теги по аудитории/поводу
    collected_at: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
    id: str = ""                    # Стабильный хэш (заполняется автоматически)

    def __post_init__(self):
        self.category = normalize_category(self.category)
        if self.category == "Прочее":
            self.category = infer_category(self.title, self.description)
        self.city = normalize_city(self.city)
        if not self.tags:
            self.tags = infer_tags(self.title, self.description,
                                   self.category, self.deal_type)
        if not self.id:
            self.id = self._make_id()

    def _make_id(self) -> str:
        base = f"{self.source}|{self.title}|{self.url}".lower()
        return hashlib.sha1(base.encode("utf-8")).hexdigest()[:16]

    # --- Актуальность ----------------------------------------------------

    def days_left(self) -> Optional[int]:
        """Сколько дней до окончания (None если дата неизвестна)."""
        if not self.ends_at:
            return None
        try:
            end = date.fromisoformat(self.ends_at[:10])
        except ValueError:
            return None
        return (end - date.today()).days

    def is_active(self) -> bool:
        """Активно ли предложение сейчас."""
        dl = self.days_left()
        if dl is not None and dl < 0:
            return False
        if self.starts_at:
            try:
                start = date.fromisoformat(self.starts_at[:10])
                if start > date.today():
                    return False
            except ValueError:
                pass
        return True

    def freshness(self) -> str:
        """Метка актуальности для фильтра на сайте."""
        dl = self.days_left()
        if dl is None:
            return "Бессрочно"
        if dl < 0:
            return "Завершено"
        if dl <= 2:
            return "Заканчивается"
        if dl <= 7:
            return "На этой неделе"
        return "Действует"

    def to_dict(self) -> dict:
        d = asdict(self)
        d["days_left"] = self.days_left()
        d["freshness"] = self.freshness()
        d["is_active"] = self.is_active()
        return d
