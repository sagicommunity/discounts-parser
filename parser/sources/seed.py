class SeedSource(BaseSource):
    """Демо-данные. Возвращает реалистичные предложения по всему Казахстану."""

    name = "Demo (сид-данные)"

    def fetch(self) -> List[Deal]:
        deals: List[Deal] = []
        for (dtype, source, title, cat, city, disc,
             old_p, new_p, days, desc, url) in _SEED:
            deals.append(Deal(
                title=title,
                source=source,
                url=url,
                category=cat,
                city=city,
                deal_type=dtype,
                description=desc,
                discount=disc,
                old_price=old_p,
                new_price=new_p,
                starts_at=_d(-3),
                ends_at=_d(days),
            ))
        return deals
